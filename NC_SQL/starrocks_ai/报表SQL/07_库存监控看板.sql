-- =====================================================
-- 报表7: 库存监控看板
-- 业务价值: 实时掌握库存状况，优化资金占用
-- MV依赖: MV_INVENTORY_SNAPSHOT
-- 更新频率: 每日
-- =====================================================

-- =====================================================
-- 7.1 库存总览 (按仓库)
-- =====================================================
SELECT
    仓库名称,
    COUNT(DISTINCT 商品主键) AS 商品数,
    SUM(结存数量) AS 总结存数量,
    ROUND(SUM(库存金额), 2) AS 总库存金额,
    ROUND(SUM(库存金额) / NULLIF(SUM(SUM(库存金额)) OVER (), 0) * 100, 2) AS 占比
FROM MV_INVENTORY_SNAPSHOT
GROUP BY 仓库名称
ORDER BY 总库存金额 DESC;


-- =====================================================
-- 7.2 库存总览 (按品类)
-- =====================================================
SELECT
    品牌,
    大类,
    品类,
    COUNT(DISTINCT 商品主键) AS 商品数,
    SUM(结存数量) AS 总结存数量,
    ROUND(SUM(库存金额), 2) AS 总库存金额,
    ROUND(AVG(结存单价), 2) AS 平均单价,
    ROUND(SUM(库存金额) / NULLIF(SUM(SUM(库存金额)) OVER (), 0) * 100, 2) AS 占比
FROM MV_INVENTORY_SNAPSHOT
GROUP BY 品牌, 大类, 品类
ORDER BY 总库存金额 DESC;


-- =====================================================
-- 7.3 库存明细 (按仓库+商品)
-- =====================================================
SELECT
    仓库名称,
    商品编码,
    商品名称,
    品牌,
    大类,
    品类,
    结存数量,
    ROUND(结存单价, 2) AS 结存单价,
    ROUND(库存金额, 2) AS 库存金额,
    ROUND(平均单价, 2) AS 平均单价,
    会计月份
FROM MV_INVENTORY_SNAPSHOT
ORDER BY 库存金额 DESC;


-- =====================================================
-- 7.4 库存品牌分布
-- =====================================================
SELECT
    品牌,
    COUNT(DISTINCT 商品主键) AS 商品数,
    SUM(结存数量) AS 总结存数量,
    ROUND(SUM(库存金额), 2) AS 总库存金额,
    ROUND(SUM(库存金额) / NULLIF(SUM(SUM(库存金额)) OVER (), 0) * 100, 2) AS 金额占比,
    ROUND(AVG(结存单价), 2) AS 平均单价
FROM MV_INVENTORY_SNAPSHOT
GROUP BY 品牌
ORDER BY 总库存金额 DESC;


-- =====================================================
-- 7.5 高库存预警 (库存金额TOP 50)
-- =====================================================
SELECT
    商品编码,
    商品名称,
    品牌,
    仓库名称,
    结存数量,
    ROUND(结存单价, 2) AS 结存单价,
    ROUND(库存金额, 2) AS 库存金额,
    ROW_NUMBER() OVER (ORDER BY 库存金额 DESC) AS 排名
FROM MV_INVENTORY_SNAPSHOT
WHERE 库存金额 > 0
ORDER BY 库存金额 DESC
LIMIT 50;
